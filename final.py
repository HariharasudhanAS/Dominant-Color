def nameofcol(centroid) :
    mindist = 442
    color = 'white'
    for i in range(0, len(colorlist)):
        dist = np.linalg.norm(np.array(centroid) - np.array(colorlist[i][0]))
        if dist < mindist:
            mindist = dist
            color = colorlist[i][1]
    return color

